class Personagem {
  final String nome;
  final String casa;
  final List<String> titulos;
  final String status;
  final List<String> aliados;
  final String historia;
  final String imagem;

  Personagem({
    required this.nome,
    required this.casa,
    required this.titulos,
    required this.status,
    required this.aliados,
    required this.historia,
    required this.imagem,
  });
}
